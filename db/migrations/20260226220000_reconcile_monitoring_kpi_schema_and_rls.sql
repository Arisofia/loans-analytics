  IF NEW.display_name IS NULL OR NEW.display_name = '' THEN
    NEW.display_name := initcap(replace(COALESCE(NEW.name, NEW.kpi_key), '_', ' '));
  END IF;

  IF NEW.updated_at IS NULL THEN
    NEW.updated_at := NOW();
  END IF;

  RETURN NEW;
END;
