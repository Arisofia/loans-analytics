CREATE OR REPLACE FUNCTION monitoring.sync_kpi_values_compat()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.kpi_key IS NULL AND NEW.kpi_id IS NOT NULL THEN
    SELECT kd.kpi_key INTO NEW.kpi_key
    FROM monitoring.kpi_definitions kd
    WHERE kd.id = NEW.kpi_id;
  END IF;

  IF NEW.value_num IS NULL AND NEW.value IS NOT NULL THEN
    NEW.value_num := NEW.value;
  ELSIF NEW.value IS NULL AND NEW.value_num IS NOT NULL THEN
    NEW.value := NEW.value_num;
  END IF;

  IF NEW.computed_at IS NULL AND NEW."timestamp" IS NOT NULL THEN
    NEW.computed_at := NEW."timestamp";
  ELSIF NEW."timestamp" IS NULL AND NEW.computed_at IS NOT NULL THEN
    NEW."timestamp" := NEW.computed_at;
  END IF;

  IF NEW.as_of_date IS NULL THEN
    NEW.as_of_date := COALESCE(NEW.computed_at, NOW())::DATE;
  END IF;

  IF NEW.snapshot_id IS NULL OR NEW.snapshot_id = '' THEN
    NEW.snapshot_id := 'manual';
  END IF;

  IF NEW.status IS NULL OR NEW.status = '' THEN
    NEW.status := 'green';
  END IF;

  IF NEW.run_id IS NULL OR NEW.run_id = '' THEN
    NEW.run_id := 'manual';
  END IF;

  IF NEW.inputs_hash IS NULL OR NEW.inputs_hash = '' THEN
    NEW.inputs_hash := 'manual';
  END IF;

  IF NEW.updated_at IS NULL THEN
    NEW.updated_at := NOW();
  END IF;

  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

