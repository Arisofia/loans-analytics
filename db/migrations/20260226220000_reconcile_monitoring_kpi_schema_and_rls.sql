-- Reconcile monitoring KPI schema drift (A/B models), harden RLS,
-- and align pipeline KPI mapping coverage.
--
-- Canonical contract adopted:
--   monitoring.kpi_definitions:
--     kpi_key (PK), id (identity), name, display_name, category, unit,
--     direction, window, basis, owner_agent, source_tables, version,
--     formula/description/thresholds, timestamps.
--   monitoring.kpi_values:
--     kpi_key + (optional kpi_id), as_of_date, value_num (+ legacy value),
--     computed_at, status, snapshot_id (TEXT, NOT NULL), run_id, inputs_hash,
--     timestamps, unique(as_of_date, kpi_key, snapshot_id).
--
-- This migration is idempotent and safe across environments that currently
-- have either model variant.

BEGIN;

CREATE EXTENSION IF NOT EXISTS pgcrypto;
CREATE SCHEMA IF NOT EXISTS monitoring;

-- -----------------------------------------------------------------------------
-- 1) Bootstrap canonical tables when missing
-- -----------------------------------------------------------------------------

CREATE TABLE IF NOT EXISTS monitoring.kpi_definitions (
  kpi_key TEXT PRIMARY KEY,
  id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  name TEXT NOT NULL,
  display_name TEXT,
  description TEXT,
  formula TEXT,
  category TEXT NOT NULL DEFAULT 'Pipeline',
  unit TEXT NOT NULL DEFAULT 'unknown',
  direction TEXT NOT NULL DEFAULT 'higher_is_better',
  "window" TEXT NOT NULL DEFAULT 'current',
  basis TEXT NOT NULL DEFAULT 'portfolio',
  green NUMERIC,
  yellow NUMERIC,
  red NUMERIC,
  threshold_warning NUMERIC,
  threshold_critical NUMERIC,
  owner_agent TEXT NOT NULL DEFAULT 'monitoring',
  source_tables TEXT[] NOT NULL DEFAULT ARRAY[]::TEXT[],
  version TEXT NOT NULL DEFAULT 'v1',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS monitoring.kpi_values (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  kpi_id BIGINT,
  kpi_key TEXT NOT NULL,
  as_of_date DATE NOT NULL,
  value NUMERIC,
  value_num NUMERIC,
  value_json JSONB,
  "timestamp" TIMESTAMPTZ,
  computed_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  status TEXT NOT NULL DEFAULT 'green',
  snapshot_id TEXT NOT NULL DEFAULT 'manual',
  run_id TEXT NOT NULL DEFAULT 'manual',
  inputs_hash TEXT NOT NULL DEFAULT 'manual',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  UNIQUE (as_of_date, kpi_key, snapshot_id)
);

-- -----------------------------------------------------------------------------
-- 2) Reconcile kpi_definitions columns/defaults
-- -----------------------------------------------------------------------------

ALTER TABLE IF EXISTS monitoring.kpi_definitions
  ADD COLUMN IF NOT EXISTS id BIGINT GENERATED BY DEFAULT AS IDENTITY,
  ADD COLUMN IF NOT EXISTS name TEXT,
  ADD COLUMN IF NOT EXISTS display_name TEXT,
